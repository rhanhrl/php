<?php
//strlen digunakan untuk menghitung panjang nilai string
echo strlen("Raihan Herlambang");

// str_word_count digunakan untuk menghitung kata dalam string
echo str_word_count("Raihan Herlambang");

// strrev berfungsi untuk mereverse nilai string
echo strrev("Raihan Herlambang");

//strpos digunakan untuk mencari kata dalam sebuah string
echo strpos("Raihan Herlambang", "Herlambang");

//str_replace berfungsi untuk merubah kata yang ada di dalam string
echo str_replace("Herlambang", "is my name", "Raihan Herlambang");

?>